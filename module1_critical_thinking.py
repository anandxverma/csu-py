# Part 1: Addition and Substraction
print("This program calculates the sum and difference of two integers n1 and n2 as n1+n1, and n1-n2")
n1 = int(input("Enter the first number n1: "))
n2 = int(input("Enter the second number n2: "))

print ("Sum of n1 and n2 =", n1 + n2)
print ("Difference of n1 and n2 =", n1 - n2)
print()

# Part 2: Multiplication and Division
print("This program calculates the multiplication and division of two integers n1 and n2 as n1*n1, and n1/n2")
n1 = int(input("Enter the first number n1: "))
n2 = int(input("Enter the second number n2: "))

print ("Multiplication of n1 and n2 =", n1 * n2)
print ("Division of n1 by n2 =", n1 / n2)
